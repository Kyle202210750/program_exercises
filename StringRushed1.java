public class StringRushed {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        String givenString = "Kyle Alojado";
        char indexChar1 = givenString.charAt(0);
        char indexChar2 = givenString.charAt(5);

        System.out.println("Character from given index1 is: " + indexChar1);
        System.out.println("Character from given index2 is: " + indexChar2);
        
        // Output:
        // Character from given index1 is: K
        // Character from given index2 is: A
    }
}
